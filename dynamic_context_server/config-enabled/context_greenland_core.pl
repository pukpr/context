ou_profile(Amp, Decay, K, I) :-
    I is Amp/(1+(K/Decay)^2).

ou_ac(Amp, Decay, T, P) :-
    P is Amp*exp(-T/Decay).


%%   navigate(+Request)
%
%    Dynamic page to ice core data
navigate(Request) :-
   collect_unit_options(time, Tunits),

   reply_html_page(cliopatria(default),
                   [title('Greenland Ice Core Data'),
	           script([type('text/javascript'),src('/html/js/submit.js')], [])
                   ],
                   [\(con_text:table_with_iframe_target(
                                    Request,
		     [
                      h1('Plot time series data'),
                      form([action(plot), target(target_iframe)],
			 [
			  select([name('t_units')], Tunits),
			  input([type('text'),
				 name('limit'),
				 value('0')]), i(' <= margin'),
			  br([]),
			  \(con_text:radio_toggles(
					 'evaluate',
					 [['DYE3', 'dye3'],
					  ['other', 'other']
                                         ])),
                          br([]),
			  input([type('submit'), name(kind), value('plot'),
                                 onclick('subm(this.form,"target_iframe");')]),
			  input([type('submit'), name(kind), value('fft'),
                                 onclick('subm(this.form,"render");')]),
			  input([type('submit'), name(kind), value('corr'),
                                 onclick('subm(this.form,"render");')])
			 ]
                          ),

                      br([]),
		      \(con_text:render_iframe(render))
                     ]
						       ))
     ]
		  ).



%%   plot(+Request)
%
%    Plot the time series
plot(Request) :-
    http_parameters(Request, [kind(fft, []),
			      limit(_Limit, [number]),
                              t_units(_TUnits, []),
                              evaluate(_Table, [])]),
    dye3_data(D0),
    D unbias D0,
    FFT fft D,
    Span ordinal FFT,
    Model mapdot ou_profile(1.3,1200) ~> Span,
    Data tuple Span + FFT + Model,

    reply_html_page([title('FFT of Time Series'),
                     \(con_text:style)],
                    [
		     \(context_graphing:dygraph_native(log, [date, data, model],
						       ['Wavenumber', k],
                                                       ['PSD', psd],
						       'PSD', Data))
                    ]
		  ).

plot(Request) :-
    http_parameters(Request, [kind(corr, []),
			      limit(_Limit, [number]),
                              t_units(_TUnits, []),
                              evaluate(_Table, [])]),
    dye3_data(D0),
    D unbias D0,
    AC0 correlate D*D,
    AC offset AC0 - 2000,
    Span ordinal AC,
    Model mapdot ou_ac(1,1000) ~> Span,
    Data tuple Span + AC + Model,

    reply_html_page([title('FFT of Time Series'),
                     \(con_text:style)],
                    [
		     \(context_graphing:dygraph_native(lin, [date, data, model],
						       ['time', k],
                                                       ['AC', prob],
						       'AC', Data))
                    ]
		  ).



plot(Request) :-
    http_parameters(Request, [kind(plot, []),
			      limit(_Limit, [number]),
                              t_units(_TUnits, []),
                              evaluate(Characteristic, [default(dye3)])]),

    (
       Characteristic  = dye3 ->
         dye3_data(D0),
         D unbias D0,
         Span ordinal D,
         Data tuple Span + D
    ;
       Characteristic = others  ->
         true
    ),
    X = 'Age',
    XUnits = ' year',
    YUnits = ' value',
    reply_html_page([title('Time series'),
                     \(con_text:style)],
                    [
		     \(context_graphing:dygraph_native(lin,
						       [year,value],
						       [X,XUnits],
                                                       ['Temperature', YUnits],
						       Characteristic, Data))
                    ]
		  ).

