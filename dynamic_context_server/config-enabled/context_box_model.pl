plot(Request) :-
    http_parameters(Request, [kind(_Kind, []),
			      limit(_Limit, [number]),
                              t_units(TUnits, []),
                              evaluate(Characteristic, [])]),

    temperature(T),
    soi(SOI),
    volcanos(V),
    volcano_number(W),
    S0 unbias SOI,
    S1 mapdot 0.085 .* S0,  % scale SOI
    expsm(S1, 0.85, S2),    % smooth SOI
    Offset = 0.22,
    S mapdot Offset .+ S2,
    length(T, L),
    H range [1, L]/1,
    BL mapdot emulated_gt(255e-12,3.0) ~> H, % 235
    % BL mapdot emulated_gt(95e-10) ~> H,
    Mod0 mapdot BL - S,
    Zeros mapdot 0 .* H,
    sparse_list(Zeros, V, Vol),
    V0 mapdot 0.54 .* Vol,  % 0.55
    W0 mapdot 1.01 .* W,
    expsm(V0, 0.978, V1),   % 0.98
    expsm(W0, 0.978, W1),
    % W2 shrink W1/V1,
    %length(V1, LV),
    %length(W1, LW),
    %print(user_error, [LV,LW]),
    VV mapdot V1 + W1,
    Mod mapdot Mod0 - VV,
    X = 'Time',
    Y1 = 'Temperature',
    Y2 = 'SOI',
    XUnits = TUnits,
    YUnits = 'C',
    Y mapdot H ./ 12,
    Year mapdot 1880 .+ Y,
    corrcoeff(T, Mod, RMS),
    scale(Characteristic, Log),
    (
       Characteristic = power ->
         Data tuple Year + T + Mod + BL,
         Header = [X, Y1, Y2, trend]
     ;
       Characteristic = residual ->
         R mapdot T - Mod,
         % Initial mapdot T - BL,
         % I mapdot Offset .+ Initial,
         Data tuple Year + R, % + I,
         Header = [X, Y1] % , Y2]
      ;
       Characteristic = model ->
         Smooth = 0.95,
	 expsm(T, Smooth, T1),
	 % uniform(3,Window),
	 % T1 window T*Window,
         R mapdot  S + VV,
	 expsm(R, Smooth, R1),
	 Residual mapdot R1 + T1,
         Data tuple Year + Residual + BL,
         Header = [X, Y2, trend]
      ;
       Characteristic = soi ->
         S3 mapdot -1 .* S2,
         Data tuple Year + S3,
         Header = [X, Y2]
      ;
       Characteristic = fft ->
         FFT fft SOI,
         Range ordinal FFT,
	 % R mapdot log_scaler ~> Range,
	 EModel mapdot delayed_exp_model(48,0.9,24,35) ~> Range,
         Data tuple Range + FFT + EModel,
         Header = [X, fft, model]
    ),
    reply_html_page([title('GISS and SOI'),
                     \(con_text:style)],
                    [
		     i('Corr Coeff = '), b('~4f ' - RMS),
		     \(context_graphing:dygraph_native(Log, Header,
						       [X,XUnits], [Y1, YUnits],
						       'GISS and SOI', Data))
                    ]
		  ).



