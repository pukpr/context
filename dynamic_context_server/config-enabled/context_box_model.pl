emulated_gt(Scale,Exp,X,Y) :-
   Y is Scale*X^Exp.

log_scaler(0,0) :- !.
log_scaler(X,Y) :-
   log(X,V),
   log(10,Ten),
   Y is V/Ten.

delayed_exp_model(Scale,BG,A,B,X,Y) :-
   Y is Scale*A*A/((X-A*sin(X/B))^2 + A*A*(1+cos(X/B))^2) + BG.

