			 [
			  select([name('t_units')], Tunits),
			  input([type('text'),
				 name('limit'),
				 value('0')]), i(' <= margin'),
			  br([]),
			  \(con_text:radio_toggles(
					 'evaluate',
					 [['Cumulative', 'cumulative'],
					  ['Daily', 'daily'],
					  ['Single Well', 'well']
                                         ])),
                          br([]),
