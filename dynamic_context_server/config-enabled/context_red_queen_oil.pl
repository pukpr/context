/*
cumulative_diffusional_dispersion(Amp, D, Theta, Year, Result) :-
    Tau is (1-exp(-Theta*Year))/Theta,
    Result is Amp/(1+sqrt(D/(Tau+0.0001))).
*/


%%   navigate(+Request)
