		  ).



%%   plot(+Request)
%
%    Plot the Red Queen model profile
plot(Request) :-
    http_parameters(Request, [kind(table, []),
			      limit(_Limit, [number]),
                              t_units(_TUnits, []),
                              evaluate(_Table, [])]),
    number_of_wells(Number_Wells),
    length(Number_Wells, L),
    Time range [0, L]/1,
    Data tuple Time + Number_Wells,

    reply_html_page([title('Shock Extraction'),
                     \(con_text:style)],
                    [
		     \(context_graphing:dygraph_native(lin, [date, rate],
						       ['Date', month],
                                                       ['Barrels', cumulative],
						       'Red Queen', Data))
                    ]
		  ).



plot(Request) :-
    http_parameters(Request, [kind(plot, []),
			      limit(Offset, [number]),
                              t_units(_TUnits, []),
                              evaluate(Characteristic, [default(cumulative)])]),

    number_of_wells(Number_Wells),
    length(Number_Wells, L),
    Time range [1, L]/1,

    Single_Well mapdot context_bakken:cumulative_diffusional_dispersion(6.97, 75000, 0.001) ~> Time,
    Monthly_Wells derivative Number_Wells/Time,
    Total convolve Single_Well*Monthly_Wells,
    (
       Characteristic  = cumulative ->
         daily_bakken(P),
         CP accumulate P,
         Factor is 365/12,
	 CData mapdot Factor .* CP,
         Total_Margin mapdot Total .- Offset,
	 Data tuple Time + Total_Margin + CData,
         Heading = [time,   'Model', 'Data'],
         CC = 0
    ;
       Characteristic = daily  ->
         daily_bakken(P),
         T ordinal Total,
         Months mapdot 1 .+ T,
         Monthly  derivative Total/Months,
         Daily mapdot Monthly ./ 30.0,
	 Data tuple Time + Daily + P,
	 Model shrink Daily/P,
         % corrcoeff(P, Model, CC),
         sum_of_squares(P, Model, CC),
         % CC = 1.0,
         Heading = [time,   'Model', 'Actual']
    ;
       Characteristic = well  ->
	 Data tuple Time + Single_Well,
         Heading = [time,   'Model'],
         CC = 0
    ),
    X = 'time',
    XUnits = ' month',
    YUnits = ' million barrels',
    reply_html_page([title('Red Queen'),
                     \(con_text:style)],
                    [
		     i('CC='), b('~8f ' - CC),
		     \(context_graphing:dygraph_native(lin,
						       Heading,
						       [X,XUnits],
                                                       ['Production', YUnits],
						       Characteristic, Data))
                    ]
		  ).




