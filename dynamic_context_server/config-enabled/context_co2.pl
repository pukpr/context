plot(Request) :-
    http_parameters(Request, [kind(Kind, []),
			      sensitivity(Sensitivity, [number]),
                              t0(T0, [number]),
                              evaluate(Characteristic, [default(shocked)])]),

    carbon_emissions(Raw),
    [Year, Carbon, _,_,_,_,_,_]  split Raw,
    Sens is Sensitivity/log(2),

    co2_knmi(Mauna_Loa),
    % co2_atm(Mauna_Loa),
    [_Year_Short, CO2_ML] split Mauna_Loa,
    % CO2_Empty range [1751, 1957]/1,
    % Zeros_CO2 mapdot 0.0 ~> CO2_Empty,
    % CO2_Mauna_Loa cat [Zeros_CO2, CO2_ML],
    % CO2_Data shrink CO2_Mauna_Loa/Year,
    CO2_Data shrink CO2_ML/Year,

    best_margins(BEST),
    [_, T_BEST] split BEST,
    [BEST_LOW,BEST_NOM,BEST_HIGH] split T_BEST,
    BEST_Empty range [1751,1799]/1,
    Zero_Val is -T0,
    Zeros_BEST mapdot Zero_Val ~> BEST_Empty,
    Temperature_LOW cat [Zeros_BEST, BEST_LOW],
    Temperature_Data cat [Zeros_BEST, BEST_NOM],
    Temperature_HIGH cat [Zeros_BEST, BEST_HIGH],
    % Temperature_Data shrink Temperature_BEST/Year,
    Low_Co2 mapdot activation(2511, 295.4, 288) ~> Temperature_LOW,
    Base_Co2 mapdot activation(2511, 295.4, 288) ~> Temperature_Data,
    High_Co2 mapdot activation(2511, 295.4, 288) ~> Temperature_HIGH,
    % Takahashi Ea=3500, C0=296, tau=sqrt(20)
    % Carroll Ea=2511, C0=295.4, tau=sqrt(24)
    Timeline range [0,1000]/1,
    Time shrink Timeline/Year,
    % H mapdot diffusive(13) ~> Time,
    % H mapdot diffu(36.8) ~> Time,
    H mapdot diffu(24) ~> Time, % 24
    % H mapdot exp_lag(45.3) ~> Time,
    (
       Characteristic = carbon  ->
	 % C_Low mapdot 100 .+ Carbon,
         % C_High mapdot 1000 .+ Carbon,
         % C_Error tuple C_Low + Carbon + C_High,
	 % Data tuple_list Year + C_Error,
         Data tuple Year + Carbon,
         Heading = ['Year', 'Carbon'],
         YUnits = ' million metric tons',
         RMS = -999.99,
         Bars =  false
    ;
       Characteristic  = co2 ->
         Scale is 1.0/2120,  % PPM / million tons
         CO2_Equivalent mapdot Scale .* Carbon,
         CO2 convolve CO2_Equivalent*H,
         CO2_Fit shrink CO2/Year,

         CO2_Low mapdot Low_Co2 + CO2_Fit,
         CO2_Total mapdot Base_Co2 + CO2_Fit,
         CO2_High mapdot High_Co2 + CO2_Fit,

	 CO2_Margin tuple CO2_Low + CO2_Total + CO2_High,
         CO2_Data_Margin tuple CO2_Data + CO2_Data + CO2_Data,

	 Data tuple_list Year + CO2_Margin + CO2_Data_Margin,
	 % Data tuple Year + CO2_High + CO2_Data,
         rms(Year, CO2_Total, CO2_Data, RMS),
         Heading = ['Year', 'Carbon model', 'CO2 data'],
         YUnits = ' PPM',
         Bars =  true
    ;
       Characteristic = temperature  ->
         Scale is 1.0/2120,  % PPM / million tons
         CO2_Equivalent mapdot Scale .* Carbon,
         CO2 convolve CO2_Equivalent*H,
         CO2_Fit shrink CO2/Year,

         CO2_Low mapdot Low_Co2 + CO2_Fit,
         CO2_Total mapdot Base_Co2 + CO2_Fit,
         CO2_High mapdot High_Co2 + CO2_Fit,

         Temperature_Minus mapdot log_scale(294,Sens) ~> CO2_Low,
         Temperature mapdot log_scale(294,Sens) ~> CO2_Total,
         Temperature_Plus mapdot log_scale(294,Sens) ~> CO2_High,

         T_Data_Low mapdot T0 .+ Temperature_LOW,
         T_Data mapdot T0 .+ Temperature_Data,
         T_Data_High mapdot T0 .+ Temperature_HIGH,

         Temperature_Margin tuple Temperature_Minus + Temperature + Temperature_Plus,
         T_Margin tuple T_Data_Low + T_Data + T_Data_High,

	 Data tuple_list Year + Temperature_Margin + T_Margin,
         rms(Year, CO2_Total, CO2_Data, RMS),
         Heading = ['Year', 'T from CO2', 'BEST'],
         YUnits = ' C',
         Bars =  true

    ),
    X = 'Date',
    XUnits = ' year',
    (	Kind = plot ->
    reply_html_page([title(Characteristic),
                     \(con_text:style)],
                    [
		      div([id('legend')],[]),
		     \(context_graphing:dygraph_error_bars(lin,
						       Heading,
						       [X,XUnits], ['Amount', YUnits],
						       Characteristic, Data, Bars)),
		     i('RMS error = '), b('~3f ' - RMS), i('PPM')
                    ]
		  )
    ;
      reply_html_page([title(Characteristic),
                       \(con_text:style)],
                      [
                       \(con_text:table_multiple_entries(
                                      [['Year', Characteristic, 'Data']],
                                      Data
                                                        ))
                      ]
                     )

    ).


