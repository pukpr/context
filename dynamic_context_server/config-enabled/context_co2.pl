rms(_,[],_,N,N, Result,Result).
rms(_,_,[],N,N, Result,Result).
rms([Y|Year],[A|AA],[B|BB],N, L, Val,Result) :-
   (   Y > 1900 ->
      Sum is Val + (A-B)^2,
      Length is N + 1
   ;
      Sum = Val,
      Length = N
   ),
   rms(Year,AA,BB,Length, L, Sum,Result).

rms(Year,A,B,Result) :-
   rms(Year,A,B,0,N,0,Sq),
   Result is sqrt(Sq/N), !.
/*
exp_lag(Mean, X, Y) :-
    Y = (exp(-X/Mean)+exp(-(X+1.0)/Mean))/2.
    % Y = 1.0/Mean * (exp(-X/Mean)+exp(-(X+1.0)/Mean))/2.
*/
log_scale(T0, Sens, X, Y) :-
    Y is Sens*log(X/T0).

hyperbolic(Median, X, Y) :-
    Y is Median/(Median+X)^2.

evaluate_diffusive(Median, X, Y) :-
    A is sqrt(Median/X),
    Y is A/2/X/(A+1)^2.

sine_wave(Theta, F, Amp, X, Y) :-
    Y is Amp*sin(X*F+Theta).

diffusive(Median, X, Y) :-
    (	X = 0 ->
        Y is 1/(1+sqrt(Median/1))
    ;
        X1 is X + 1,
        evaluate_diffusive(Median, X, Y0),
	evaluate_diffusive(Median, X1, Y1),
	Y is (Y0+Y1)/2
    ).

diffu(Median, X, Y) :-
   Y is (1/(1+sqrt(X/Median))+1/(1+sqrt((X+1)/Median)))/2.

activation(Energy, Conc, Kelvin, X, Y) :-
   Y is Conc*exp(-Energy/(Kelvin+X))/exp(-Energy/Kelvin).

